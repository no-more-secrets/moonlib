-----------------------------------------------------------------
-- Printing methods.
-----------------------------------------------------------------
local M = {}

-----------------------------------------------------------------
-- Aliases.
-----------------------------------------------------------------
local format = string.format

-----------------------------------------------------------------
-- Implementation.
-----------------------------------------------------------------
function M.printf( fmt, ... )
  assert( fmt )
  io.write( format( fmt, ... ) )
end

function M.printfln( fmt, ... )
  M.printf( fmt .. '\n', ... )
end

-----------------------------------------------------------------
-- Finished.
-----------------------------------------------------------------
return M
