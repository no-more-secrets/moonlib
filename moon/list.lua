-----------------------------------------------------------------
-- List methods.
-----------------------------------------------------------------
local M = {}

-----------------------------------------------------------------
-- Implementation.
-----------------------------------------------------------------
function M.join( lst, sep )
  local res = ''
  local cur_sep = ''
  for _, elem in ipairs( lst ) do
    res = res .. cur_sep
    cur_sep = sep
    res = res .. tostring( elem )
  end
  return res
end

-----------------------------------------------------------------
-- Finished.
-----------------------------------------------------------------
return M
