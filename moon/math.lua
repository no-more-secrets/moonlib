-----------------------------------------------------------------
-- Math-related functions.
-----------------------------------------------------------------
-----------------------------------------------------------------
-- Aliases.
-----------------------------------------------------------------
local min, max = math.min, math.max

-----------------------------------------------------------------
-- Implementation.
-----------------------------------------------------------------
local function clamp( n, low, high )
  assert( low <= high )
  return min( max( n, low ), high )
end

local function shuffle( lst )
  for i = 1, #lst do
    local j = math.random( i, #lst )
    lst[i], lst[j] = lst[j], lst[i]
  end
end

-----------------------------------------------------------------
-- Finished.
-----------------------------------------------------------------
return {
  clamp=clamp, --
  shuffle=shuffle, --
}
