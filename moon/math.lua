-----------------------------------------------------------------
-- Math-related functions.
-----------------------------------------------------------------
-----------------------------------------------------------------
-- Aliases.
-----------------------------------------------------------------
local min, max = math.min, math.max

-----------------------------------------------------------------
-- Implementation.
-----------------------------------------------------------------
local function clamp( n, low, high )
  assert( low <= high )
  return min( max( n, low ), high )
end

-----------------------------------------------------------------
-- Finished.
-----------------------------------------------------------------
return {
  clamp=clamp, --
}
